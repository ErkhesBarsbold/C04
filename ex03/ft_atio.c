#include<unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	k;
	int	x;
	int	y;

	x = 0;
	y = -1;
	i = 0;
	k = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '-')
			x++;
		if (y == -1 && str[i] >= '0' && str[i] <= '9')
			k = k * 10 - 48 + str[i];
		if (k != 0 && (str[i] < '0' || str[i] > '9'))
			y = 1;
		i++;
	}
	if (x % 2 == 1)
		k = -k;
	return (k);
}
