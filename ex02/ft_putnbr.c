#include<unistd.h>

void	ft_putnbr(int nb)

{
	if (nb == -2147483648)
		printf("-2147483648");
	else
	{
		if (nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		if (nb >= 10)
			ft_putnbr(nb / 10);
		nb = nb % 10 + 48;
		write(1, &nb, 1);
	}
}
